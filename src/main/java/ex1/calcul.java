package ex1;

public class Calcul {

	/**
	 * Prend deux paramètres entiers et renvoie leur somme.
	 * 
	 * @param a le premier entier.
	 * @param b le second entier.
	 * @return le resultat de l'addition
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Prend deux paramètres entiers et renvoie le résultat de la soustraction.
	 * 
	 * @param a le premier entier.
	 * @param b le second entier.
	 * @return le resultat de la soustraction
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
